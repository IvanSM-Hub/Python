#For:
#Este bucle de repetición se caracteriza del "while" por que sé las repeticiones que quiero realizar.

numeros=(2,3,5,7,9,11,13,17,19)

for i in range(10):
    print(i)
    print("interación completada")

for i in numeros:
    print(i)
    print("interación completada")

for i in range(len(numeros)):
    print(numeros[i])
    print("interación completada")